const express = require('express')

const itemController = express.Router()

const Model = require('../models/Item')

//Post Method
itemController.post('/item', async (req, res) => {
    const reqBody = req.body
    const data = new Model({
        itemCode: reqBody.itemCode,
        itemName: reqBody.itemName,
        itemQuantity: reqBody.itemQuantity,
        selectedQuantity: reqBody.selectedQuantity,
        itemPrice: reqBody.itemPrice,
        itemImage: reqBody.itemImage
    })

    try {
        const dataToSave = await data.save();
        res.status(200).json(dataToSave)
    } catch (error) {
        res.status(400).json({ message: error.message })
    }
})

//Get all Method
itemController.get('/item', async (req, res) => {
    try {
        const data = await Model.find();
        res.json(data)
    }
    catch (error) {
        res.status(500).json({ message: error.message })
    }
})

//Get by ID Method
itemController.get('/item/:id', async (req, res) => {
    try {
        const data = await Model.findById(req.params.id);
        res.json(data)
    }
    catch (error) {
        res.status(500).json({ message: error.message })
    }
})

//Update by ID Method
itemController.put('/item/:id', async (req, res) => {
    try {
        const id = req.params.id;
        const updatedData = req.body;
        const options = { new: true };

        const result = await Model.findByIdAndUpdate(
            id, updatedData, options
        )

        res.send(result)
    }
    catch (error) {
        res.status(400).json({ message: error.message })
    }
})

//Delete by ID Method
itemController.delete('/item/:id', async (req, res) => {
    try {
        const id = req.params.id;
        const data = await Model.findByIdAndDelete(id)
        res.send(`Document with ${data.name} has been deleted..`)
    }
    catch (error) {
        res.status(400).json({ message: error.message })
    }
})

module.exports = itemController
