const express = require('express')

const inventoryController = express.Router()

const Model = require('../models/Inventory')

//Post Method
inventoryController.post('/inventory', async (req, res) => {
    const reqBody = req.body
    const data = new Model({
        itemNo: reqBody.itemNo,
        itemName: reqBody.itemName,
        dueDate: reqBody.dueDate,
        quantity: reqBody.quantity,
        noOfItems: reqBody.noOfItems
    })

    try {
        const dataToSave = await data.save();
        res.status(200).json(dataToSave)
    } catch (error) {
        res.status(400).json({ message: error.message })
    }
})

//Get all Method
inventoryController.get('/inventory', async (req, res) => {
    try {
        const data = await Model.find();
        res.json(data)
    }
    catch (error) {
        res.status(500).json({ message: error.message })
    }
})

//Get by ID Method
inventoryController.get('/inventory/:id', async (req, res) => {
    try {
        const data = await Model.findById(req.params.id);
        res.json(data)
    }
    catch (error) {
        res.status(500).json({ message: error.message })
    }
})

//Update by ID Method
inventoryController.put('/inventory/:id', async (req, res) => {
    try {
        const id = req.params.id;
        const updatedData = req.body;
        const options = { new: true };

        const result = await Model.findByIdAndUpdate(
            id, updatedData, options
        )

        res.send(result)
    }
    catch (error) {
        res.status(400).json({ message: error.message })
    }
})

//Delete by ID Method
inventoryController.delete('/inventory/:id', async (req, res) => {
    try {
        const id = req.params.id;
        const data = await Model.findByIdAndDelete(id)
        res.send(`Document with ${data.name} has been deleted..`)
    }
    catch (error) {
        res.status(400).json({ message: error.message })
    }
})

module.exports = inventoryController
